    </div>

    <div class="container politica-info">
        <div class="row">
            <div class="col-xs-12 col-sm-12 col-md-10 col-md-offset-1">

                <p>
                    Este documento tem por objetivo demonstrar a preocupação da CANPUIG em proteger a privacidade e os dados pessoais de todos os seus clientes, parceiros e colaboradores, estando alinhada com as melhores práticas de mercado e com todos os princípios do artigo 6º da Lei Geral de Proteção de Dados – LGPD, solicitando sempre que necessário a manifestação livre, informada e inequívoca do titular para a coleta e tratamento de seus dados pessoais, informando sempre a finalidade de tratamento, em cumprimento com a Lei nº 13.709/2018 – Lei Geral de Proteção de Dados Pessoais (LGPD).
                </p>
                <p>1, <strong>Definições</strong></p>
                <p>
                    <strong>Titular:</strong> pessoa natural a quem se referem os dados pessoais que são objeto de tratamento;
                </p>
                <p>
                    <strong>Controlador:</strong> pessoa natural ou jurídica, de direito público ou privado, a quem competem as decisões referentes ao tratamento de dados pessoais;
                </p>
                <p>
                    <strong>Operador:</strong> pessoa natural ou jurídica, de direito público ou privado, que realiza o tratamento de dados pessoais em nome do controlador.
                </p>
                <p>
                    <strong>Dados Pessoais:</strong> são todos os dados fornecidos ou coletados pela Jerônimo daVeiga que permitam a identificação do Usuário. Não são considerados dados pessoais, números de telefone fixo ou de celulares comerciais ou e-mails comerciais.
                </p>
                <p>
                    <strong>Finalidade:</strong> é o objetivo para o qual os Dados Pessoais dos Usuários são coletados/tratados.
                </p>
                <p>
                    <strong>Necessidade:</strong> é a justificativa pela qual se coleta/trata os Dados Pessoais dos Usuários para atingir a Finalidade desejada.
                </p>
                <p>
                    <strong>Consentimento:</strong> autorização expressa e inequívoca do titular do Dado Pessoal para a sua coleta/tratamento para atingir uma Finalidade específica e previamente descrita.
                </p>
                <p>
                    <strong>Tratamento:</strong> toda operação realizada com dados pessoais, como as que se referem a coleta, produção, recepção, classificação, utilização, acesso, reprodução, transmissão, distribuição, processamento, arquivamento, armazenamento, eliminação, avaliação ou controle da informação, modificação, comunicação, transferência, difusão ou extração;
                </p>
                <p>
                    <strong>Bases legais:</strong> fundamento jurídico que autoriza a utilização dos Dados Pessoais para o cumprimento da Finalidade.
                </p>
                <p>
                    <strong>1. Quem é o responsável pela coleta e tratamento dos dados pessoais de nossos clientes, parceiros e colaboradores?</strong>
                    <br /><br />
                    A sociedade CANPUIG EMPREENDIMENTOS IMOBILIÁRIOS LTDA., inscrita no CNPJ: 05.977.253/0001-33 com sede na Rua Jerônimo da Veiga, nº 164, 1º andar, sala E, São Paulo/SP, é a responsável pela coleta e tratamento dos dados pessoais de seus clientes, parceiros e colaboradores, assumindo a função de CONTROLADORA de dados nos termos das definições acima indicadas
                </p>
                <p>
                    <strong>2. Quais são as informações que coletamos e como coletamos?</strong>
                    <br /><br />
                    A CANPUIG coleta os dados pessoais dos seus clientes quando estes buscam informações sobre seus empreendimentos, seja por meio dos plantões de vendas ou canais online/remoto de atendimento. O titular preenche uma ficha cadastral outorgando de forma expressa e inequívoca o seu consentimento para as finalidades neste documento mencionadas. Nesse cadastro, o próprio indivíduo informa nome completo, e-mail, telefone, data de nascimento, renda, CPF (titular e cônjuge), RG, Passaporte (se estrangeiro), estado civil, FGTS, vínculo empregatício, profissão e endereço completo.
                    <br />
                    Quem acessa e interage com a CANPUIG por meio da internet e ingressa em seu website, também tem outros dados coletados automaticamente, como: características do dispositivo de acesso, do navegador, IP (com data e hora), origem do IP, informações sobre cliques, páginas acessadas, termos de pesquisa digitados em nosso site, desde que autorizada de forma específica. Para a coleta desses dados, a CANPUIG utiliza de tecnologias padrões do mercado, como por exemplo, os cookies (pacote de dados enviados por um site de internet para o navegador do usuário, quando este visita o site).
                </p>
                <p>
                    <strong>3. Qual é a finalidade do tratamento?</strong>
                    <br /><br />
                    Para desenvolver a sua atividade econômica e concluir todas as etapas do procedimento de oferta, divulgação, compra, gestão do contrato, escrituração e financiamento da sua unidade, bem como para verificações e validações internas, como, por exemplo, verificação de cadastro e checagens em órgãos de proteção ao crédito.
                    A CANPUIG também poderá tratar dados pessoais com base em seu interesse legítimo, sempre no limite de sua finalidade e necessidade, e nunca em prejuízo dos interesses, direitos e liberdades fundamentais dos titulares de dados.
                    Além disso, por meio dos dados coletados, podemos divulgar as ofertas dos nossos produtos por e-mails, mensagens de texto via SMS, via WhatsApp, bem como realizar telefonemas, desde que aprovados expressamente no momento da coleta. A CANPUIG esclarece ao titular dos dados que poderá cancelar o recebimento das ofertas e/ou publicidade enviado um e-mail para dpo@jveiga.com.br.
                </p>
                <p>
                    <strong>4. Utilização de cookies:</strong>
                    <br /><br />
                    A CANPUIG utiliza cookies com o mesmo propósito que a maioria dos provedores de serviços de Internet utilizam, ou seja, para melhorar a experiência do usuário, facilitando e simplificando a sua experiência de acesso. Fazendo isso, podemos entender seus hábitos de navegação, para que possamos reconhecer sua identidade quando você acessar o site da CANPUIG. A intenção da utilização de cookies é proporcionar um ambiente mais amigável ao titular oferecendo produtos e aplicações que estejam diretamente ligadas aos seus interesses e necessidades, facilitando o acesso, as ofertas de empreendimentos que melhor atendam às suas expectativas.
                    <br /><br />
                    Os cookies são pequenos arquivos de texto, utilizados por websites para tornar a navegação mais eficiente. Em nosso website são utilizados os seguintes cookies:
                    <br /><br />
                    <strong>•</strong> Necessários: são aqueles relacionados a funções básicas do website, permitindo o funcionamento adequado de navegação e proteção de acesso.<br />
                    <strong>•</strong> Marketing: são utilizados com a intenção de exibir anúncios que sejam relevantes aos interesses do visitante, por meio do armazenamento de informações referentes aos hábitos de navegação dos usuários.<br />
                    <strong>•</strong> Analíticos: Os cookies nos ajudam a entender como os visitantes interagem com a página da CANPUIG, fornecendo informações sobre as áreas visitadas, o tempo de visita ao site e quaisquer problemas encontrados, como mensagens de erro.<br />
                    <strong>•</strong> Funcionais: permitem que sejam armazenadas informações de preferência de navegação e comportamento do website, tais como idioma e localização.
                </p>


                <p>
                    <strong>5. Por quanto tempo armazenamos as suas informações?</strong>
                    <br /><br />
                    Durante todo período que o titular possuir relação contratual com a CANPUIG e por mais 06 (seis) meses após o final da relação contratual, como determina o artigo 15 do Marco Civil da Internet, bem como durante o tempo necessário para o cumprimento das obrigações pertinentes aos fins que motivaram sua coleta, bem como enquanto não forem superados todos os prazos prescricionais legais a que estiver submetida.
                    <br /><br />
                    A CANPUIG compromete-se a utilizar todas as medidas técnicas e organizacionais apropriadas para proteger os dados pessoais, levando em conta técnicas avançadas disponíveis, o contexto e as finalidades do tratamento. As medidas de segurança atenderão as (i) exigências das leis de proteção de dados; e (ii) medidas de segurança correspondentes com as boas práticas de mercado.
                    <br /><br />
                    Os dados pessoais tratados pela CANPUIG serão eliminados assim que a relação contratual se extinguir e quando for solicitado pelo titular do dado, enviando e-mail para dpo@jveiga.com.br, exceto se a manutenção do dado for expressamente autorizada por Lei ou regulação aplicável.
                </p>
                <p>
                    <strong>6. Com quem compartilhamos seus dados?</strong>
                    <br /><br />
                    Os Dados coletados e as atividades registradas podem ser compartilhados:
                    <br /><br />
                    (i) Com autoridades judiciais, administrativas ou governamentais competentes, sempre que houver determinação legal, requerimento, requisição ou ordem judicial
                    <br />
                    (ii) Para proteção dos interesses da CANPUIG em processos arbitrais, judiciais e administrativos;
                    <br />
                    (iii) Com instituições responsáveis pela cobrança ou proteção ao crédito decorrente da aquisição da(s)unidade(s) imobiliária(s) que o titular tenha adquirido;
                    <br />
                    (iv) Com o corretor de imóveis, quando a compra for intermediada, garantindo este a confidencialidade dos dados por escrito;
                    <br />
                    (v) Instituições financeiras de financiamento de imóvel;
                    <br />
                    (vi) Com a administradora do condomínio, que prestará os serviços de gestão e administração do empreendimento; e
                    <br />
                    (vii) Em caso de movimentações societárias, como fusão, aquisição e incorporação, sempre informando os titulares de dados;
                    <br />
                    (vii) Securitização de recebíveis.
                    <br /><br />
                    Além disso, para a operacionalização da sua atividade, a CANPUIG vale-se da parceria com diversas empresas prestadoras de serviços para o desenvolvimento do produto ofertado a você, como imobiliárias e corretores, hipótese em que também pode ocorrer o compartilhamento.
                </p>
                <p>
                    <strong>7. Quais os seus direitos enquanto titular de dados pessoais?</strong>
                    <br /><br />
                    A CANPUIG respeita e garante todos os direitos previstos no artigo 18 da LGPD, dentre eles a:
                    <br /><br />
                    i) Confirmação da existência de tratamento; ii) Correção de dados incompletos, inexatos ou desatualizados; iii) Anonimização, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade com a lei; iv) Portabilidade dos dados a outro fornecedor de serviço ou produto, mediante requisição expressa pelo Cliente; v) Eliminação dos dados tratados com consentimento do Cliente; vi) Obtenção de informações sobre as entidades públicas ou privadas com as quais a CANPUIG compartilhou os seus dados; vii) Informação sobre a possibilidade de o Cliente não fornecer o consentimento, bem como de ser informado sobre as consequências em caso de negativa; viii) Revogação do consentimento.
                    <br /><br />
                    Todos estes direitos poderão ser exercidos livremente pelo titular de dados, bastando o envio de comunicação por escrito ao e-mail dpo@jveiga.com.br.
                </p>
                <p>
                    <strong>8. Como exercer seus direitos enquanto titular de dados pessoais?</strong>
                    <br /><br />
                    A CANPUIG nomeou como seu Encarregado de Dados, o Sr. Marcello Borghi Raymundo que poderá ser contatado por meio do e-mail dpo@jveiga.com.br ou pelo endereço na Rua Jerônimo da Veiga, nº 164, 7º andar, conj. “F”, Itaim Bibi, CEP 04536-000, São Paulo/SP, para pedidos de esclarecimentos ou remoção de dados.
                </p>
                <p>
                    <strong>9. Como a JERÔNIMO DA VEIGA protege seus dados pessoais?</strong>
                    <br /><br />
                    A CANPUIG adota políticas internas de sigilo e privacidade, bem como políticas de segurança da informação, levando em consideração as melhores práticas de mercado, com tecnologia disponível e razoavelmente acessível, para atender a LGPD e outras leis de proteção de dados pessoais, As políticas de segurança estão adequadas para manter os dados pessoais seguros e protegidos de acessos não autorizados e de situações acidentais ou ilícitas de destruição, perda, alteração, comunicação ou qualquer forma de tratamento inadequado ou ilícito.
                </p>
                <p>
                    <strong>10. Dever de sigilo e confidencialidade</strong>
                    <br /><br />
                    A CANPUIG compromete-se a cumprir com o dever de sigilo e confidencialidade a respeito das informações e dados pessoais coletados e tratados que estejam sob seu controle e responsabilidade, de acordo com a legislação vigente.
                </p>
                <p>
                    <strong>11. Atualização deste aviso de privacidade, legislação aplicável e foro competente</strong>
                    <br /><br />
                    Esta Política de Privacidade pode ser alterada periodicamente, mas todas as versões serão arquivadas para consulta de seus usuários.
                    <br /><br />
                    Essa Política será interpretada segundo a legislação brasileira, sendo eleito o foro Central da Capital do Estado de São Paulo para dirimir qualquer controvérsia que envolva este documento, saldo ressalva específica de competência pessoal, territorial ou funcional pela legislação aplicável.
                </p>

                <p>
                    <strong>SEUS DIREITOS E SOLICITAÇÕES</strong>
                </p>
                <p>
                    <strong>Como titular de seus dados pessoais, você pode exercer seus diretos e solicitar as opções abaixo.</strong>
                </p>
                <p>
                    <strong>Eliminação de dados pessoais (Direito ao esquecimento)</strong>
                    <br /><br />
                    Você, como titular dos dados, tem o direito à eliminação dos seus dados pessoais tratados pelo controlador, desde que o mesmo não tenha uma obrigação de mantê-los
                </p>

                <p>
                    <strong>Confirmação da existência de tratamento e acesso aos dados</strong>
                    <br /><br />
                    Você, como titular dos dados, tem o direito de obter do responsável pelo tratamento, sem demora injustificada, os dados pessoais que o mesmo possui a seu respeito e também a confirmação da existência de tratamento dos mesmos.
                </p>
                <p>
                    <strong>Acesso a seus dados pessoais</strong>
                    <br /><br />
                    Você, como titular dos dados, tem o direito de acesso aos dados pessoais que foram coletados em relação a ele e exercer esse direito facilmente e em intervalos razoáveis, a fim de estar ciente e verificar a legalidade do processamento.
                </p>
                <p>
                    <strong>Correção de dados incompletos, inexatos ou desatualizados</strong>
                    <br /><br />
                    Você, como titular dos dados, tem o direito de obter do responsável pelo tratamento, sem demora injustificada, a retificação de dados pessoais incorretos que lhe digam respeito. Tendo em conta os fins do tratamento, o titular dos dados tem o direito de fornecer dados pessoais incompletos, incluindo através de uma declaração suplementar.
                </p>
                <p>
                    <strong>Portabilidade de dados</strong>
                    <br /><br />
                    Você, como titular dos dados, tem o direito de receber os dados pessoais a seu respeito, que forneceu a um controlador, em um formato estruturado, comumente estruturado, comumente usado e legível por máquina, e terá o direito de transmitir esses dados para outro controlador sem impedimento do controlador para o qual os dados pessoais foram fornecidos.
                </p>
                <p>
                    Todos os direitos acima mencionados poderão ser livremente exercidos pelo titular do dado enviando comunicação por escrito ao e-mail dpo@jveiga.com.br
                </p>
                <p>
                    São Paulo, 20 de outubro de 2020.
